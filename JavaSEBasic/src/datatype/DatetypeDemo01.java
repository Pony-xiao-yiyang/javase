package datatype;

/**
 * @description:八大数据类型
 * @author:
 * @time: 2022/10/25 20:05
 */
public class DatetypeDemo01 {
    public static void main(String[] args) {
        //八大数据类型

        //整数
        Integer integer =   1;//包装类
        Byte byte1 =  1;
        int     num1    =   10;//最常用
        byte    num2   =   20;
        short   num3  =   30;
        long    num4    =   30L;//Long类型要在数字后面加L

        //小数，浮点数
        float num5  =   40.2f;
        double num6 =   1.1314d;

        //字符
        char name = 'A';

        //字符串,string不是关键字，类
        String name1    =   "adolf";

        //布尔值
        boolean flag = true;
        boolean flag1 = false;




    }
}
